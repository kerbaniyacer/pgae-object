class Command(BaseCommand):
    help = "تعبئة قاعدة البيانات بقيم الخصائص (AttributeValue) لكل فئة"

    def add_arguments(self, parser):
        parser.add_argument(
            '--clean',
            action='store_true',
            help='حذف جميع قيم الخصائص الموجودة قبل التعبئة',
        )

    def handle(self, *args, **kwargs):
        clean = kwargs.get('clean', False)

        if clean:
            self.stdout.write("🧹 جاري حذف قيم الخصائص القديمة...")
            AttributeValue.objects.all().delete()
            self.stdout.write(self.style.WARNING("✓ تم حذف قيم الخصائص القديمة"))

        self.stdout.write("⚙️  جاري إضافة قيم الخصائص...")

        total_created = 0
        total_skipped = 0

        for subcat_name, attributes in ATTRIBUTE_VALUES.items():

            # البحث عن الفئة الفرعية (قد تكون في أي فئة رئيسية)
            subcategories = Category.objects.filter(
                name=subcat_name,
                parent__isnull=False
            )

            if not subcategories.exists():
                self.stdout.write(
                    self.style.WARNING(
                        f"  ⚠️  الفئة الفرعية غير موجودة: {subcat_name}"
                    )
                )
                continue

            for subcat in subcategories:
                for attr_name, values in attributes.items():

                    # البحث عن الخاصية داخل هذه الفئة
                    try:
                        attribute = Attribute.objects.get(
                            name=attr_name,
                            category=subcat
                        )
                    except Attribute.DoesNotExist:
                        self.stdout.write(
                            self.style.WARNING(
                                f"  ⚠️  الخاصية «{attr_name}» غير موجودة في «{subcat_name}»"
                            )
                        )
                        continue

                    # إضافة القيم
                    values_to_create = []
                    for value in values:
                        exists = AttributeValue.objects.filter(
                            attribute=attribute,
                            value=value
                        ).exists()
                        if not exists:
                            values_to_create.append(
                                AttributeValue(attribute=attribute, value=value)
                            )
                        else:
                            total_skipped += 1

                    if values_to_create:
                        AttributeValue.objects.bulk_create(values_to_create)
                        total_created += len(values_to_create)

        # ─────────────────────────────────────────
        # ملخص
        # ─────────────────────────────────────────
        self.stdout.write("\n" + "=" * 50)
        self.stdout.write(self.style.SUCCESS("📊 ملخص العملية:"))
        self.stdout.write(
            self.style.SUCCESS(f"  ✅ تم إنشاء   : {total_created} قيمة جديدة")
        )
        self.stdout.write(
            self.style.WARNING(f"  ⏭️  تم تخطي   : {total_skipped} قيمة موجودة مسبقاً")
        )
        self.stdout.write(
            f"  📦 المجموع الكلي: {AttributeValue.objects.count()} قيمة"
        )
        self.stdout.write("=" * 50)
        self.stdout.write(self.style.SUCCESS("🔥 تمت التعبئة بنجاح!"))